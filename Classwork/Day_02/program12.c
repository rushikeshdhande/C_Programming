/*
    START
        Accept number and store as no
        Divide no by 2 
        If the remainder is 0
            then display as Even
        otherwise 
            display as Odd
    STOP

*/
/////////////////////////////////////////////////////////////////
//
//  Required Header Files
//  Description : This section includes necessary header files
//
/////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : CheckEvenOdd
//  Description   : This function checks whether the given
//                  integer number is Even or Odd
//  Input         : Integer
//  Output        : Displays result on screen
//  Author        : Rushikesh Baban Dhande
//  Date          : 09/10/2025
//
/////////////////////////////////////////////////////////////////

void CheckEvenOdd(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Local variable to store remainder
    /////////////////////////////////////////////////////////////////

    int iRem = 0;

    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Find remainder when number is divided by 2
    /////////////////////////////////////////////////////////////////

    iRem = iNo % 2;

    /////////////////////////////////////////////////////////////////
    // Check condition and display result
    /////////////////////////////////////////////////////////////////

    if(iRem == 0)
    {
        printf("It is Even number\n");
    }
    else
    {
        printf("It is Odd number\n");
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts a number from the user
//                and checks whether it is Even or Odd using
//                user-defined function
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter number : ");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to check Even or Odd
    /////////////////////////////////////////////////////////////////

    CheckEvenOdd(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

