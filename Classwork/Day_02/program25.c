#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function prints the message
//                  "Jay Ganesh..." multiple times using loop
//  Input         : void
//  Output        : void
//
//  Note          : Comment mentions "Static Function" but the
//                  function is declared without static keyword.
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
