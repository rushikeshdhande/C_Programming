/////////////////////////////////////////////////////////////////

// Static Function

void Display()
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Loop to print message multiple times
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= 4; iCnt++)
    {
        printf("Jay Ganesh...\n");
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program calls the Display function
//                to print messages on the screen
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{      
    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display();  

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

