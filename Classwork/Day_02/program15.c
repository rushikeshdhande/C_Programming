#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : CheckEvenOdd
//  Description   : This function checks whether the given
//                  integer number is Even or Odd
//  Input         : Integer
//  Output        : Boolean value
//                  true  -> if number is Even
//                  false -> if number is Odd
//
/////////////////////////////////////////////////////////////////

bool CheckEvenOdd(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Check remainder when number is divided by 2
    /////////////////////////////////////////////////////////////////

    if((iNo % 2) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts a number from the user,
//                calls a function to check Even or Odd and
//                displays the result based on returned value
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;
    bool bRet = false;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter number : ");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to check Even or Odd
    /////////////////////////////////////////////////////////////////

    bRet = CheckEvenOdd(iValue);

    /////////////////////////////////////////////////////////////////
    // Display result based on boolean value
    /////////////////////////////////////////////////////////////////

    if(bRet == true)
    {
        printf("%d is Even number\n", iValue);
    }
    else
    {
        printf("%d is Odd number\n", iValue);
    }

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

