#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function prints the message
//                  "Jay Ganesh..." number of times based
//                  on the frequency provided by the user.
//                  It also checks for invalid input using filter.
//  Input         : Integer (Frequency)
//  Output        : void
//
//  Note          : Function is termed as Dynamic Function because
//                  number of iterations depends on user input.
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

// Dynamic Function

void Display(int iFrequency)
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Filter
    // Check for invalid input
    /////////////////////////////////////////////////////////////////

    if(iFrequency < 0)
    {
        printf("Invalid input\n");
        return;
    }

    /////////////////////////////////////////////////////////////////
    // Loop to print message based on valid frequency
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= iFrequency; iCnt++)
    {
        printf("Jay Ganesh...\n");
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts frequency from the user
//                and calls Display function with filter logic
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{ 
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iCount = 0;
    
    /////////////////////////////////////////////////////////////////
    // Accept frequency from user
    /////////////////////////////////////////////////////////////////

    printf("Enter the frequency : \n");
    scanf("%d",&iCount);

    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display(iCount);  
    
    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

