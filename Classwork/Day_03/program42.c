#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays even numbers
//                  from 1 to the given number by checking
//                  each number using modulus operator
//  Input         : Integer (iNo)
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Loop to traverse numbers from 1 to iNo
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        /////////////////////////////////////////////////////////////////
        // Check if number is even
        /////////////////////////////////////////////////////////////////

        if((iCnt % 2) == 0)
        {
            printf("%d\t", iCnt);
        }
    }

    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Time Complexity : O(N)
//  Reason          : Loop runs from 1 to iNo once
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Please enter frequency : \n");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

