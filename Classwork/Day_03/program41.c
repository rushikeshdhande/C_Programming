#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays even numbers
//                  from 2 up to the given number
//                  using for loop with increment of 2
//  Input         : Integer (iNo)
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Loop to print even numbers from 2 to iNo
    /////////////////////////////////////////////////////////////////

    for(iCnt = 2; iCnt <= iNo; iCnt += 2)
    {
        printf("%d\t", iCnt);
    }

    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Time Complexity : O(N/2)
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Please enter frequency : \n");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

