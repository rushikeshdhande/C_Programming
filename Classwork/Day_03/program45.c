#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactors
//  Description   : This function displays the factors of
//                  number 6 by checking divisibility with
//                  fixed values from 1 to 5
//  Input         : void
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void DisplayFactors()
{
    /////////////////////////////////////////////////////////////////
    // Check divisibility of number 6 with fixed numbers
    /////////////////////////////////////////////////////////////////

    if((6 % 1) == 0)
    {
        printf("1\n");
    }   
    if((6 % 2) == 0)
    {
        printf("2\n");
    }   
    if((6 % 3) == 0)
    {
        printf("3\n");
    }   
    if((6 % 4) == 0)
    {
        printf("4\n");
    }   
    if((6 % 5) == 0)
    {
        printf("5\n");
    }   
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program calls DisplayFactors function
//                to print factors of number 6
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Function call to DisplayFactors
    /////////////////////////////////////////////////////////////////

    DisplayFactors();

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

