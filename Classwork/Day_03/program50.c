#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactors
//  Description   : Displays all factors of the given number
//                  except the number itself using loop
//  Input         : Integer (iNo)
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void DisplayFactors(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Updator
    // Convert negative number to positive
    /////////////////////////////////////////////////////////////////

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    /////////////////////////////////////////////////////////////////
    // Loop to check factors from 1 to iNo/2
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= (iNo / 2); iCnt++)
    {
        /////////////////////////////////////////////////////////////////
        // Check whether iCnt is a factor of iNo
        /////////////////////////////////////////////////////////////////

        if((iNo % iCnt) == 0)
        {
            printf("%d\n", iCnt);
        }   
    }
}

/////////////////////////////////////////////////////////////////
//
//  Time Complexity : O(N/2)
//
/////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Accepts number from user and displays its factors
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter number : \n");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to DisplayFactors
    /////////////////////////////////////////////////////////////////

    DisplayFactors(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

