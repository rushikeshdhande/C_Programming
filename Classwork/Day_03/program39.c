#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays numbers from
//                  the given number to 0 in reverse order
//                  using for loop with tab spacing
//  Input         : Integer (iNo)
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Loop to print numbers from iNo to 0
    /////////////////////////////////////////////////////////////////

    for(iCnt = iNo; iCnt >= 0; iCnt--)
    {
        printf("%d\t", iCnt);
    }

    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts frequency from user
//                and calls Display function to print
//                numbers in reverse order including zero
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept frequency from user
    /////////////////////////////////////////////////////////////////

    printf("Please enter frequency : \n");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

