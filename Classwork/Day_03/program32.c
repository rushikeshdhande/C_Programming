#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays numbers from 1 to 5
//                  with '*' symbol after each number in a
//                  single line using sequential statements
//  Input         : void
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display()
{
    /////////////////////////////////////////////////////////////////
    // Print numbers and '*' symbol sequentially
    /////////////////////////////////////////////////////////////////

    printf("1\t*\t");
    printf("2\t*\t");
    printf("3\t*\t");
    printf("4\t*\t");
    printf("5\t*\t");
    
    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program calls Display function to
//                print number and symbol pattern
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display();

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

