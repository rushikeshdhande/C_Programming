#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays numbers from 1 to 5
//                  with '*' symbol after each number using
//                  for loop
//  Input         : void
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display()
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;
    
    /////////////////////////////////////////////////////////////////
    // for loop structure
    // 1 : Initialization
    // 2 : Condition
    // 3 : Increment
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        /////////////////////////////////////////////////////////////////
        // 4 : Loop body
        // Print number followed by '*' symbol
        /////////////////////////////////////////////////////////////////

        printf("%d\t*\t", iCnt);
    }
    
    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program calls Display function
//                to print number and symbol pattern
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display();

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

