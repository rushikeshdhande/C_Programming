#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : CheckDivisible
//  Description   : This function checks whether the first
//                  number is completely divisible by the
//                  second number
//  Input         : Integer, Integer
//  Output        : Boolean (true / false)
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

bool CheckDivisible(int iNo1, int iNo2)
{
    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Check divisibility using modulus operator
    /////////////////////////////////////////////////////////////////

    if((iNo1 % iNo2) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts two numbers from user,
//                calls CheckDivisible function and displays
//                the result based on returned boolean value
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue1 = 0, iValue2 = 0;
    bool bRet = false;

    /////////////////////////////////////////////////////////////////
    // Accept first number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter first number : \n");
    scanf("%d",&iValue1);

    /////////////////////////////////////////////////////////////////
    // Accept second number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter second number : \n");
    scanf("%d",&iValue2);

    /////////////////////////////////////////////////////////////////
    // Function call to check divisibility
    /////////////////////////////////////////////////////////////////

    bRet = CheckDivisible(iValue1, iValue2);

    /////////////////////////////////////////////////////////////////
    // Display result based on returned value
    /////////////////////////////////////////////////////////////////

    if(bRet == true)
    {
        printf("It is completely divisible\n");
    }    
    else
    {
        printf("It is not divisible\n");
    }

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

