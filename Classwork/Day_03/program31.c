#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : This function displays the '*' symbol
//                  five times in a single line using for loop
//                  with tab spacing
//  Input         : void
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

void Display()
{
    /////////////////////////////////////////////////////////////////
    // Loop counter variable
    /////////////////////////////////////////////////////////////////

    int iCnt = 0;

    /////////////////////////////////////////////////////////////////
    // Loop to print '*' symbol five times
    /////////////////////////////////////////////////////////////////

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("*\t");
    }

    /////////////////////////////////////////////////////////////////
    // Move cursor to next line
    /////////////////////////////////////////////////////////////////

    printf("\n");
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program calls Display function
//                to print symbols on the screen
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Function call to Display
    /////////////////////////////////////////////////////////////////

    Display();

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

