#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactors
//  Description   : This function displays factors of the
//                  given number by checking divisibility
//                  with fixed values from 1 to 5.
//                  This is termed as a wrong approach because
//                  it does not scale for all numbers.
//  Input         : Integer (iNo)
//  Output        : void
//  Author        : Rushikesh Baban Dhande
//  Date          : 10/10/2025
//
/////////////////////////////////////////////////////////////////

// Wrong approach
void DisplayFactors(int iNo)
{
    /////////////////////////////////////////////////////////////////
    // Check divisibility with fixed numbers
    /////////////////////////////////////////////////////////////////

    if((iNo % 1) == 0)
    {
        printf("1\n");
    }   
    if((iNo % 2) == 0)
    {
        printf("2\n");
    }   
    if((iNo % 3) == 0)
    {
        printf("3\n");
    }   
    if((iNo % 4) == 0)
    {
        printf("4\n");
    }   
    if((iNo % 5) == 0)
    {
        printf("5\n");
    }   
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts a number from user
//                and calls DisplayFactors function
//  Author      : Rushikesh Baban Dhande
//  Date        : 10/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue = 0;

    /////////////////////////////////////////////////////////////////
    // Accept number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter number : \n");
    scanf("%d",&iValue);

    /////////////////////////////////////////////////////////////////
    // Function call to DisplayFactors
    /////////////////////////////////////////////////////////////////

    DisplayFactors(iValue);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

