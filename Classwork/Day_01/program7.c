/*
    Algorithm

    START
        Accept first number as no1
        Accept second number as no2
        If the input is negative then convert it into positive
        Perform Addition of no1 & no2
        Display the addition on screen
    STOP
*/

/////////////////////////////////////////////////////////////////
//
//  Required Header Files
//  Description : This section includes necessary header files
//
/////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : AdditionTwoNumbers
//  Description   : This function performs addition of two integer
//                  numbers and returns the result
//  Input         : Integer, Integer
//  Output        : Integer
//  Author        : Rushikesh Baban Dhande
//  Date          : 09/10/2025
//
/////////////////////////////////////////////////////////////////

int AdditionTwoNumbers(int iNo1, int iNo2)
{
    /////////////////////////////////////////////////////////////////
    // Local variable to store addition result
    /////////////////////////////////////////////////////////////////

    int iSum = 0;

    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Perform addition of two numbers
    /////////////////////////////////////////////////////////////////

    iSum = iNo1 + iNo2;

    /////////////////////////////////////////////////////////////////
    // Return result to calling function
    /////////////////////////////////////////////////////////////////

    return iSum;
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts two numbers from the user
//                and performs addition using user-defined function
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int iValue1 = 0, iValue2 = 0, iRet = 0;

    /////////////////////////////////////////////////////////////////
    // Accept first number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter first number : \n");
    scanf("%d",&iValue1);

    /////////////////////////////////////////////////////////////////
    // Accept second number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter second number : \n");
    scanf("%d",&iValue2);
    
    /////////////////////////////////////////////////////////////////
    // Function call to perform addition
    /////////////////////////////////////////////////////////////////

    iRet = AdditionTwoNumbers(iValue1, iValue2);

    /////////////////////////////////////////////////////////////////
    // Display the result
    /////////////////////////////////////////////////////////////////

    printf("Addition is : %d\n", iRet);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

