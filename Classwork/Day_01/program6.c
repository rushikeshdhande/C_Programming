/////////////////////////////////////////////////////////////////
//
//  Required Header Files
//  Description : This section includes necessary header files
//
/////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : Add
//  Description   : This function performs addition of two integers
//  Input         : Integer, Integer
//  Output        : Integer
//  Author        : Rushikesh Baban Dhande
//  Date          : 09/10/2025
//
/////////////////////////////////////////////////////////////////

int Add(int a, int b)
{
    /////////////////////////////////////////////////////////////////
    // Local variable to store addition result
    /////////////////////////////////////////////////////////////////

    int sum = 0;

    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Perform addition of two numbers
    /////////////////////////////////////////////////////////////////

    sum = a + b;

    /////////////////////////////////////////////////////////////////
    // Return result to calling function
    /////////////////////////////////////////////////////////////////

    return sum;
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts two numbers from user
//                and performs addition using user-defined function
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int i = 0, j = 0, ans = 0;

    /////////////////////////////////////////////////////////////////
    // Accept first number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter first number : \n");
    scanf("%d",&i);

    /////////////////////////////////////////////////////////////////
    // Accept second number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter second number : \n");
    scanf("%d",&j);
    
    /////////////////////////////////////////////////////////////////
    // Function call to perform addition
    /////////////////////////////////////////////////////////////////

    ans = Add(i, j);

    /////////////////////////////////////////////////////////////////
    // Display the result
    /////////////////////////////////////////////////////////////////

    printf("Addition is : %d\n", ans);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

