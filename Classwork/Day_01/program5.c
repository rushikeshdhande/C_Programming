#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program performs addition of two numbers
//                accepted from the user
//  Author      : Rushikesh Baban Dhande
//  Date        : 09/10/2025
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    int i = 0, j = 0, ans = 0;

    /////////////////////////////////////////////////////////////////
    // Accept first number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter first number : \n");
    scanf("%d",&i);

    /////////////////////////////////////////////////////////////////
    // Accept second number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter second number : \n");
    scanf("%d",&j);
    
    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Perform addition of two numbers
    /////////////////////////////////////////////////////////////////

    ans = i + j;

    /////////////////////////////////////////////////////////////////
    // Display the result
    /////////////////////////////////////////////////////////////////

    printf("Addition is : %d\n", ans);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

