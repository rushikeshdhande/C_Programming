/*
    Algorithm

    START
        Accept first number as no1
        Accept second number as no2
        If the input is negative then convert it into positive
        Perform Addition of no1 & no2
        Display the addition on screen
    STOP
*/
/////////////////////////////////////////////////////////////////
//
//  Required Header Files
//  Description : This section includes necessary header files
//
/////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Function Name : AdditionTwoNumbers
//  Description   : This function performs addition of two
//                  floating point numbers.
//                  It converts negative inputs into positive
//                  values before performing addition.
//  Input         : Float, Float
//  Output        : Float
//  Author        : Rushikesh Baban Dhande
//  Date          : 09/10/2025
//
/////////////////////////////////////////////////////////////////

float AdditionTwoNumbers(float fNo1, float fNo2)
{
    /////////////////////////////////////////////////////////////////
    // Local variable to store addition result
    /////////////////////////////////////////////////////////////////

    float fSum = 0.0f;

    /////////////////////////////////////////////////////////////////
    // Updator Logic
    // Convert negative input values into positive values
    /////////////////////////////////////////////////////////////////

    if(fNo1 < 0.0f)
    {
        fNo1 = -fNo1;
    }

    if(fNo2 < 0.0f)
    {
        fNo2 = -fNo2;
    }

    /////////////////////////////////////////////////////////////////
    // Business Logic
    // Perform addition of two positive floating point numbers
    /////////////////////////////////////////////////////////////////

    fSum = fNo1 + fNo2;
    
    /////////////////////////////////////////////////////////////////
    // Return result to calling function
    /////////////////////////////////////////////////////////////////

    return fSum;
}

/////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//  Description : Execution of program starts from main()
//                This program accepts two floating point numbers
//                from the user and performs addition using
//                user-defined function with updator logic
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    // Variable Declaration
    /////////////////////////////////////////////////////////////////

    float fValue1 = 0.0f, fValue2 = 0.0f, fRet = 0.0f;

    /////////////////////////////////////////////////////////////////
    // Accept first number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter first number : \n");
    scanf("%f",&fValue1);

    /////////////////////////////////////////////////////////////////
    // Accept second number from user
    /////////////////////////////////////////////////////////////////

    printf("Enter second number : \n");
    scanf("%f",&fValue2);
    
    /////////////////////////////////////////////////////////////////
    // Function call to perform addition
    /////////////////////////////////////////////////////////////////

    fRet = AdditionTwoNumbers(fValue1, fValue2);

    /////////////////////////////////////////////////////////////////
    // Display the result
    /////////////////////////////////////////////////////////////////

    printf("Addition is : %f\n", fRet);

    /////////////////////////////////////////////////////////////////
    // Return 0 indicates successful execution of program
    /////////////////////////////////////////////////////////////////

    return 0;
}

